package Util;

public class ZSum {
	
	public String math(long L){
		
		String str = null;
		if(L>=0 && L<1024){
			str="KB";
			 String a = Long.toString(L);
			
			 str = a + str;
		}else if(L>1024){
			str="M";
			
			float L2 = (float)L/1024;
			String a2 = Float.toString(L2);
			
			str = a2.substring(0,5) + str;
			
		}
		return str;
	}
	


}




